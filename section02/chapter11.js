// 자바스크립트의 "동기(Synchronous)"란 여러 개의 작업(Task)을
// 위에서 아래로 순서대로 하나씩 처리하는 방식을 말한다.
//
// 예시:
// Task A → Task B → Task C
// 이 순서로 차례대로 실행된다.
//
// 하지만 단점은,
// 만약 Task B가 매우 오래 걸리는 작업(예: 100초)이면,
// 뒤에 오는 Task C는 그 작업이 끝날 때까지 기다려야 한다.
// 즉, 전체 실행 흐름이 "막힌다(Blocking)"는 문제가 있다.
//
// 자바스크립트 엔진은 '단일 쓰레드(single thread)'로 동작하기 때문에
// 동시에 여러 작업을 병렬로 처리할 수 있는 멀티쓰레드 방식처럼 해결이 되지 않는다.
//
// 이런 한계를 보완하기 위해 JS는 "비동기(Asynchronous)" 처리를 지원한다.
// 비동기 방식에서는 시간이 오래 걸리는 작업을 백그라운드에서 처리하고,
// 나머지 코드들은 계속 실행된다.
//
// 그리고 이렇게 비동기적으로 실행된 함수의 결과(return 값 등)를
// 나중에 사용해야 할 때는 "콜백(callback)" 함수를 이용할 수 있다.

// -----------------------------------------------------------------------------------
console.log(1);
// (1) 동기 코드 → 즉시 실행됨
// 출력: 1

setTimeout(
    () => {console.log(2);}
    , 3000);
// (2) 비동기 코드
// setTimeout()은 지정한 시간이 지난 후 콜백 함수를 실행하도록 예약하는 함수.
// 즉, 3초(3000ms) 후에 console.log(2)가 실행되도록 "예약"한다.
// 이때, JS 엔진은 3초를 기다리지 않고 다음 코드를 먼저 실행한다.

console.log(3);
// (3) 동기 코드 → 바로 실행됨
// 출력: 3

// 1 (즉시) → 3 (즉시) → 2 (3초 후)

    // 쓰레드가 하나인데도 이렇게 여러 개의 작업을 처리할 수 있는 이유는,
    // setTimeout 같은 함수들이 자바스크립트 엔진의 쓰레드에서 직접 실행되는 것이 아니라,
    // 브라우저가 관리하는 Web APIs라는 별도의 영역에서 처리되기 때문이다.
    //
    // 이 Web APIs는 간단히 말해 웹 브라우저가 직접 관리하는 별도의 공간이고,
    // 여기서 실제로 자바스크립트의 비동기 작업들이 실행된다.
    //
    // 즉, setTimeout 같은 함수를 만나면 자바스크립트 엔진은
    // 브라우저의 Web APIs에게 “타이머를 실행해 달라” 라고 요청하고,
    // 함수의 끝나는 시점에 실행할 콜백을 함께 넘겨준다.
    // 이후 자바스크립트 엔진은 타이머를 기다리지 않고,
    // 그 아래에 있는 다른 작업들을 바로 이어서 실행한다.
    //
    // 웹 APIs의 타이머가 끝나면,
    // 자바스크립트 엔진에 전달받았던 콜백 함수를 다시 돌려주고,
    // 자바스크립트 엔진은 그 콜백을 받아 실행함으로써 비동기 처리가 이루어진다.
